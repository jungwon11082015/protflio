     let tl1 = gsap.timeline({
        scrollTrigger: {
          trigger: ".banner-container card",
          start: "top 20px",
          end: "600px top",
          // scrub:1,
          markers:true,
          toggleActions: "restart pause restart pause"
        }
      });
